public class People {
    protected int id;
    protected String name;
    protected String sex;
    protected int age;

    public People(int id, String name, String sex,int age){
        this.id = id;
        this.name = name;
        this.sex = sex;
        this.age = age;

    }
    public String getName(){
         return name;
    }
}
       
    
